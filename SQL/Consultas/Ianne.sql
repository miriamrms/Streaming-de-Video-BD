        (SELECT * FROM CONTA C 
         WHERE C.ID_PLANO = P.ID);

/* CONSULTA 50 - PROJETAR OS NOMES DOS PLANOS QUE POSSUEM CONTAS VINCULADAS – USANDO “IN” */
SELECT P.NOME
FROM PLANO P
WHERE P.ID IN
        (SELECT C.ID_PLANO  
         FROM CONTA C 
         WHERE C.ID_PLANO IS NOT NULL);

/* CONSULTA 51 - PROJETAR OS TÍTULOS DOS EPISÓDIOS COM DURAÇÃO ABAIXO DA MÉDIA DE DURAÇÃO DOS EPISÓDIOS DA SUA PRÓPRIA SÉRIE (SUBCONSULTA CORRELACIONADA) */
SELECT E1.TITULO
FROM EPISODIO E1 INNER JOIN VIDEO V1 ON (E1.ID_VIDEO = V1.ID)
WHERE V1.DURACAO <  
            (SELECT AVG(V2.DURACAO)
             FROM EPISODIO E2 INNER JOIN VIDEO V2 ON (E2.ID_VIDEO = V2.ID)
             WHERE E1.ID_SERIE = E2.ID_SERIE);

