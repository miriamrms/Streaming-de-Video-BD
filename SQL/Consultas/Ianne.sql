/* CONSULTA 21 - PROJETAR O TÍTULO DE TODOS OS CONTEÚDOS E A CATEGORIA ASSOCIADA  SEM USAR “INNER JOIN” */
SELECT C.TITULO, CAT.CATEGORIA
FROM CONTEUDO C, CATEGORIAS CAT
WHERE C.ID = CAT.ID_CONTEUDO;

/* CONSULTA 22 - PROJETAR O TÍTULO DE TODOS OS CONTEÚDOS E A CATEGORIA ASSOCIADA - USANDO “INNER JOIN” */
SELECT C.TITULO, CAT.CATEGORIA
FROM CONTEUDO C INNER JOIN CATEGORIAS CAT ON (C.ID = CAT.ID_CONTEUDO);

/* CONSULTA 23 - PROJETAR OS NOMES DE TODOS OS ARTISTAS E OS TÍTULOS DOS CONTEÚDOS EM QUE ATUARAM CUJA CLASSIFICAÇÃO SEJA 'Livre' OU '12 Anos' - SEM USAR “INNER JOIN” */
SELECT A.NOME, C.TITULO
FROM ARTISTA A, ELENCO E, CONTEUDO C
WHERE A.CPF = E.CPF AND 
      E.ID_CONTEUDO = C.ID AND 
      C.CLASSIFICACAO IN ('Livre', '12 Anos');

/* CONSULTA 24 - PROJETAR OS NOMES DE TODOS OS ARTISTAS E OS TÍTULOS DOS CONTEÚDOS EM QUE ATUARAM CUJA CLASSIFICAÇÃO SEJA 'Livre' OU '12 Anos' 
- USANDO “INNER JOIN” */
SELECT A.NOME, C.TITULO
FROM ARTISTA A INNER JOIN ELENCO E ON (A.CPF = E.CPF) 
     INNER JOIN CONTEUDO C ON (E.ID_CONTEUDO = C.ID)
WHERE C.CLASSIFICACAO IN ('Livre', '12 Anos');

/* CONSULTA 25 - PROJETAR TODOS OS CONTEÚDOS (TÍTULOS) E O ID DO VIDEO (SE HOUVER) */
SELECT C.TITULO, F.ID_VIDEO
FROM CONTEUDO C LEFT OUTER JOIN FILME F ON (C.ID = F.ID_CONTEUDO);

/* CONSULTA 26 - PROJETAR TODOS OS CONTEÚDOS (TÍTULOS) E O ID DO VIDEO (SE HOUVER) */
SELECT C.TITULO, F.ID_VIDEO
FROM FILME F RIGHT OUTER JOIN CONTEUDO C ON (C.ID = F.ID_CONTEUDO);

/* CONSULTA 27 - PROJETAR AS CONTAS (NOMES) E SEUS RESPECTIVOS DISPOSITIVOS (MODELOS), INCLUINDO AS CONTAS SEM DISPOSITIVOS E OS DISPOSITIVOS SEM CONTAS */
SELECT C.NOME, D.MODELO
FROM CONTA C FULL OUTER JOIN DISPOSITIVO D ON (C.EMAIL = D.EMAIL);

/* CONSULTA 28 - PROJETAR AS CONTAS (NOMES) SEM DISPOSITIVOS REGISTRADOS (NULL PARA MODELO) – USANDO “LEFT OUTER JOIN” */
SELECT C.NOME, D.MODELO
FROM CONTA C LEFT OUTER JOIN DISPOSITIVO D ON (C.EMAIL = D.EMAIL)
WHERE D.MODELO IS NULL;

/* CONSULTA 29 - PROJETAR AS CONTAS (NOMES) SEM DISPOSITIVOS REGISTRADOS (NULL PARA MODELO) - USANDO “RIGHT OUTER JOIN” */
SELECT C.NOME, D.MODELO
FROM DISPOSITIVO D RIGHT OUTER JOIN CONTA C ON (C.EMAIL = D.EMAIL)
WHERE D.MODELO IS NULL;

/* CONSULTA 30 - PROJETAR CONTAS SEM DISPOSITIVOS E DISPOSITIVOS SEM CONTAS VINCULADAS - USANDO “FULL OUTER JOIN” */
SELECT C.NOME, D.MODELO
FROM CONTA C FULL OUTER JOIN DISPOSITIVO D ON (C.EMAIL = D.EMAIL)
WHERE C.EMAIL IS NULL OR D.NUM_SERIE IS NULL;

/* CONSULTA 31 - PARA CADA ARTISTA, PROJETAR SEU NOME E 
OS NOMES DOS ARTISTAS MAIS NOVOS DO QUE ELE */
SELECT A1.NOME AS ARTISTA_MAIS_VELHO, A2.NOME AS ARTISTA_MAIS_NOVO
FROM ARTISTA A1 INNER JOIN ARTISTA A2 ON (A1.DATA_NASCIMENTO < A2.DATA_NASCIMENTO);

/* CONSULTA 32 - PROJETAR O NOME E O NASCIMENTO DOS ARTISTAS EM 
ORDEM DECRESCENTE DO NASCIMENTO. PARA DATAS IGUAIS, CONSIDERAR A ORDEM ALFABÉTICA DO NOME DO ARTISTA */
SELECT NOME, DATA_NASCIMENTO
FROM ARTISTA
ORDER BY DATA_NASCIMENTO DESC, NOME ASC;

/* CONSULTA 33 - PROJETAR A MÉDIA DE DURAÇÃO DOS VÍDEOS DOS FILMES POR CLASSIFICAÇÃO DO CONTEÚDO */
SELECT C.CLASSIFICACAO, AVG(V.DURACAO) AS MEDIA_DURACAO
FROM CONTEUDO C INNER JOIN FILME F ON (C.ID = F.ID_CONTEUDO)
     INNER JOIN VIDEO V ON (F.ID_VIDEO = V.ID)
GROUP BY C.CLASSIFICACAO;

/* CONSULTA 34 - PROJETAR AS CLASSIFICAÇÕES CUJAS MÉDIAS DE DURAÇÃO DOS FILMES SÃO MAIORES DO QUE 100 MINUTOS */
SELECT C.CLASSIFICACAO, AVG(V.DURACAO) AS MEDIA_DURACAO
FROM CONTEUDO C INNER JOIN FILME F ON (C.ID = F.ID_CONTEUDO)
     INNER JOIN VIDEO V ON (F.ID_VIDEO = V.ID)
GROUP BY C.CLASSIFICACAO 
HAVING AVG(V.DURACAO) > 100;

/* CONSULTA 35 - PROJETAR OS NOMES DAS CONTAS QUE POSSUEM MAIS DE 1 DISPOSITIVO REGISTRADO */
SELECT C.NOME, COUNT(*) AS QTD_DISPOSITIVOS
FROM CONTA C INNER JOIN DISPOSITIVO D ON (C.EMAIL = D.EMAIL) 
GROUP BY C.NOME
HAVING COUNT(*) > 1;

