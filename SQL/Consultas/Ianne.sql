/* CONSULTA 46 - PROJETAR O PERCENTUAL DE CONTAS POR TIPO DE PLANO */
SELECT ID_PLANO, (QTD/(SELECT COUNT(*) FROM CONTA))*100 AS PERCENTUAL
FROM 
	(SELECT ID_PLANO, COUNT(*) AS QTD
	 FROM CONTA
	 GROUP BY ID_PLANO);

/* CONSULTA 47 - PROJETAR QUAIS SÃO OS PLANOS (IDs) QUE TÊM MAIS CONTAS DO QUE A MÉDIA GERAL DE CONTAS POR PLANO */
SELECT ID_PLANO, COUNT(*) AS QTD_CONTAS
FROM CONTA
GROUP BY ID_PLANO
HAVING COUNT(*) > 
            (SELECT AVG(QTD)
             FROM (SELECT ID_PLANO, COUNT(*) AS QTD 
                   FROM CONTA
                   GROUP BY ID_PLANO));

/* CONSULTA 48 - PROJETAR OS NOMES DOS PLANOS QUE POSSUEM CONTAS VINCULADAS */
