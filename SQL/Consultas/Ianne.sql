/* CONSULTA 37 - PROJETAR OS NOMES DOS ARTISTAS QUE NÃO INTERPRETAM PERSONAGENS MAIORES DE IDADE - USANDO “IN” E “INNER JOIN” */
SELECT NOME
FROM ARTISTA 
WHERE CPF NOT IN
                (SELECT I.CPF
                 FROM INTERPRETA I INNER JOIN PERSONAGEM P ON I.ID_PERSONAGEM = P.ID
                 WHERE P.IDADE >= 18);

/* CONSULTA 38 - PROJETAR OS NOMES DOS ARTISTAS QUE NASCERAM NO MESMO ANO E TÊM O MESMO GÊNERO DO ARTISTA DE CPF '111.111.111-11' */
SELECT NOME
FROM ARTISTA 
WHERE (GENERO, TO_CHAR(DATA_NASCIMENTO,'YY')) = 
            (SELECT GENERO, TO_CHAR(DATA_NASCIMENTO,'YY')
             FROM ARTISTA
             WHERE CPF = '111.111.111-11');
            
/* CONSULTA 39 - POR GÊNERO, PROJETAR O NOME E O GÊNERO DOS ARTISTAS MAIS VELHOS */
SELECT NOME, GENERO
FROM ARTISTA
WHERE (GENERO, TO_CHAR(DATA_NASCIMENTO,'YY')) IN 
     (SELECT GENERO, MIN(TO_CHAR(DATA_NASCIMENTO,'YY'))
      FROM ARTISTA
      GROUP BY GENERO)
ORDER BY GENERO;

